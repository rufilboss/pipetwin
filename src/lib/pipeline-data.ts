function toGeo(i: number): { lat: number; lng: number } {
  const [lat, lng] = ROUTE[i] ?? ROUTE[ROUTE.length - 1];
  return { lat, lng };
}

function sliceRoute(start: number, end: number) {
  return ROUTE.slice(start, end + 1).map(([lat, lng]) => ({ lat, lng }));
}

