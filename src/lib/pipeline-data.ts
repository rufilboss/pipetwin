function makeSensor(
  id: string,
  segmentId: string,
  type: Sensor["type"],
  label: string,
  routeIndex: number,
  unit: string,
  value: number,
  baseline: number,
  warn: number,
  critical: number,
  stationId?: string
): Sensor {
  return {
    id,
    segmentId,
    stationId,
    type,
    label,
    position: toGeo(routeIndex),
    unit,
    value,
    baseline,
    threshold: { warn, critical },
    status: "operational",
    lastUpdated: new Date().toISOString(),
  };
}

